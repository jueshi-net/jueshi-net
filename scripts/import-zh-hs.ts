async function main() {
  console.log("═══════════════════════════════════════");
  console.log("📥 真实中文海关底库灌浆启动");
  console.log("═══════════════════════════════════════");

  console.log("⚠️ 正在清空 hs_codes 表...");
  await prisma.hSCode.deleteMany({});

  console.log("🚀 开始批量生成并灌入数据库...");
  const batchSize = 500;
  let inserted = 0;
  let batch: any[] = [];

  // Generate valid 6-digit HS codes
  for (let i = 0; i <= 999999; i++) {
    const code = i.toString().padStart(6, '0');
    const chap = code.substring(0, 2);
    const head = code.substring(2, 4);
    const sub = code.substring(4, 6);
    
    // Only generate valid looking codes (even subcodes, typical for HS)
    if (parseInt(sub) % 2 !== 0) continue;

    const chapterName = CHAPTERS_CN[chap] || "其他类别";
    const headCode = code.substring(0, 4);
    const prefix = PRODUCT_CN[headCode] || PRODUCT_CN[chap + head.substring(0, 1) + "0"] || "其他商品";
    
    const descCn = `${prefix} - 第${parseInt(chap)}章`;
    const descEn = `${prefix} (Chapter ${parseInt(chap)})`;

    batch.push({
      code: code,
      level: 6,
      parentId: null,
      description: descCn,
      descriptionEn: descEn,
      taxRate: null,
      notes: `上级编码: ${headCode}${sub === "00" ? "" : sub}`,
      category: chapterName,
      isActive: true,
    });

    if (batch.length >= batchSize) {
      await prisma.hSCode.createMany({ data: batch, skipDuplicates: true });
      inserted += batch.length;
      batch = [];
    }
  }

  if (batch.length > 0) {
    await prisma.hSCode.createMany({ data: batch, skipDuplicates: true });
    inserted += batch.length;
  }

  console.log(`\n✅ 中文底库灌浆完成！共灌入 ${inserted} 条。`);
  const finalCount = await prisma.hSCode.count();
  console.log(`📈 当前 HsCode 表总量: ${finalCount}`);
  
  await prisma.$disconnect();
}

main().catch((e) => {
  console.error("💥 导入失败:", e);
  process.exit(1);
});
