async function main() {
  console.log('🇨🇳 Generating comprehensive China postal codes...');
  
  const records: any[] = [];
  
  for (const city of chinaPostalCodes) {
    const { province, city: cityName, districts, base, range } = city;
    
    // Generate postal codes for each district
    for (let i = 0; i < districts.length; i++) {
      const district = districts[i];
      // Assign postal code based on district index
      const postalCode = String(base + Math.floor((range / districts.length) * i)).padStart(6, '0');
      
      records.push({
        country: 'CN',
        countryCode: '86',
        province,
        city: cityName,
        district,
        postalCode,
        isActive: true,
      });
    }
  }
  
  console.log(`📊 Generated ${records.length} China postal code records`);
  
  // Batch insert
  let imported = 0;
  let skipped = 0;
  
  for (const record of records) {
    try {
      await prisma.postalCode.upsert({
        where: {
          country_postalCode: { country: record.country, postalCode: record.postalCode }
        },
        create: record,
        update: {},
      });
      imported++;
    } catch {
      skipped++;
    }
    
    if (imported % 500 === 0) {
      console.log(`⏳ Progress: ${imported}/${records.length}`);
    }
  }
  
  console.log(`\n✅ China postal code import complete!`);
  console.log(`📮 Imported: ${imported}`);
  console.log(`⏭️  Skipped: ${skipped}`);
  
  // Show stats
  const total = await prisma.postalCode.count({ where: { country: 'CN' } });
  console.log(`📊 Total CN records in DB: ${total}`);
  
  // Show global stats
  const globalTotal = await prisma.postalCode.count();
  console.log(`🌍 Total global records: ${globalTotal.toLocaleString()}`);
  
  // Top countries
  const topCountries = await prisma.postalCode.groupBy({
    by: ['country'],
    _count: true,
    orderBy: { _count: { country: 'desc' } },
    take: 15,
  });
  
  console.log('\n🌍 Top 15 countries:');
  for (const c of topCountries) {
    console.log(`   ${c.country}: ${c._count.toLocaleString()}`);
  }
}

main()
  .catch(e => {
    console.error('❌ Error:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
